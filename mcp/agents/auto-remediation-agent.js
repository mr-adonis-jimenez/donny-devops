export function buildRemediationPlan(payload) {
  const findings = payload.findings || [];

  return findings.map((finding) => {
    if (finding.type === "secret-risk") {
      return {
        file: finding.file,
        action: "remove-secret",
        patchHint: "Move credential material to environment variables or secret manager references.",
        priority: "critical"
      };
    }

    if (finding.type === "workflow-permissions") {
      return {
        file: finding.file,
        action: "tighten-permissions",
        patchHint: "Add explicit least-privilege permissions block with only required scopes.",
        priority: "high"
      };
    }

    if (finding.type === "dependency-drift") {
      return {
        file: finding.file,
        action: "pin-dependency",
        patchHint: "Pin the dependency version and update lockfile for reproducible builds.",
        priority: "medium"
      };
    }

    return {
      file: finding.file,
      action: "manual-review",
      patchHint: "No safe automatic remediation generated. Human review required.",
      priority: "medium"
    };
  });
}

export function buildRemediationPullRequestBody(repo, plan) {
  const lines = [
    `## Auto-remediation plan for ${repo}`,
    "",
    "This PR was generated by the Donny DevOps remediation agent.",
    "",
    "### Planned changes"
  ];

  for (const item of plan) {
    lines.push(`- ${item.priority.toUpperCase()}: ${item.file} -> ${item.action}`);
    lines.push(`  - ${item.patchHint}`);
  }

  lines.push("");
  lines.push("### Guardrails");
  lines.push("- Keep blast radius narrow.");
  lines.push("- Require review before merge.");
  lines.push("- Preserve rollback path.");

  return lines.join("\n");
}
